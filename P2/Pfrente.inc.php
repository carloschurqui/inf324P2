● No haber sido condenado a pena corporal, con pérdida privativa de libertad ni tener pliego de
cargo o auto ejecutoriados. <br>
● No podrán participar aquellos que sirvieron en organismos de seguridad y represión o que
hayan incurrido en actos que lesionen los principios y fines de la Autonomía Universitaria, la
dignidad e integridad de la comunidad universitaria. <br>
● No tener proceso universitario con sentencia ejecutoriada. <br>
● No tener antecedentes vandálicos en la Universidad Mayor de San Andrés. <br>
● No ser autoridad dirigencial universitaria en funciones en otra unidad académica u otro
estamento o gremio. <br>
● En caso de presentarse a la reelección deberá presentar renuncia el candidato al momento de la
inscripción de su respectivo frente. <br>


